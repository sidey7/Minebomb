const botaoMostrar = document.getElementById("bota1");
const explicaDiv = document.getElementById("expkica");
const botafecha = document.getElementById("fechar");
const botavoltar = document.getElementById("none");

botaoMostrar.addEventListener("click", () => {
  if (window.getComputedStyle(explicaDiv).display === "none") { 
    explicaDiv.style.display = "block"; 
  } else { 
    explicaDiv.style.display = "none"; 
  }
});

botavoltar.addEventListener("click", () => {
  explicaDiv.style.display = "none";
});

